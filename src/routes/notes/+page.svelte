<p>
  Synchronous programming: Synchronous programming, often referred to as implicit or sequential, is
  where things happen one at a time. A program pauses while the action gets performed, only
  returning when it has a result. Asynchronous programming: Asynchronous programming, often called
  explicit, is where multiple things happen at the same time and within your control. The program
  continues to run even after you start a specific action and prompts when it has a result, even as
  you complete other work or tasks.
</p>
